# Question 
# The length L,breadth B and height H of a cuboid are passed as input and the program must print the volume of the cuboid as the output.

# Input format
# The first line denotes integer L
# The second line denotes integer B
# The third line denotes integer H

# Output format
# The first line denotes the volume of the cuboid

# Sample testcases
#     Input 1           Output 1
#     2                 40
#     4
#     5 

# Answer :
L = int(input())
B = int(input())
H = int(input())
Vol = L*B*H
print(Vol)