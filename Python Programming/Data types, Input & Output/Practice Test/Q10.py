# Question :
# Two numbers A and B which are co-prime to each other are passed as input.
# The program must print the numbers with a hypen between A and B.

# Input format
# The first line input consist of an integer.
# The second line input consist of an integer.

# Output format
# Output prints the numbers with a hypen between A and B.

# Sample testcases
#     Input 1           Output 1
#     2                 2-5
#     5

# Answer :
n=int(input())
m=int(input())
print("%s-%s" %(n,m))