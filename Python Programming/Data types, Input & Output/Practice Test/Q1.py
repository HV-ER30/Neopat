# Question 
# The length l and breadth b of a rectangle as passed as input.
# The program must calculate the perimeter of the rectangle and print the perimeter as the output.
# Both L and B are positive integers.

#Input format
#The first line denote the length L
#The second line denote the breadth B

#Output format
#Output displays the perimeter of rectangle

#Sample testcases
#         Input 1            Output 1
#         20                 60
#         10

# Answer : 
L = int(input())
B = int(input())
Perimeter = 2*(L+B)
print(Perimeter)