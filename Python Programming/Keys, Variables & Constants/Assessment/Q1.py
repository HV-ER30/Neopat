# Question :
# Raju borrowed Rs.20000 from Ramu at an interest rate of 5%. 
# Write a python program to find the simple Interest for % units of time. 
# (Simple Interest is given by SI = (P*T*R)/100 where P is the principal amount, T is the time, R is the rate)

# Input format :
# Input is directly given in the program

# Output format :
# Output is the number representing simple interest.

# Sample Testcase
#     Input 1          Output 1
#                      5000.0

# Answer : 
P = 20000
R = 5
T = 5
SI = (P*T*R)/100
print(SI)